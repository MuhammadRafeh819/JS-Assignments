var a = 10;
console.log("Result:");
console.log("The value of a is: " + a);


console.log("The value of ++a is: " + (++a));
console.log("Now the value of a is: " + a + "\n");

console.log("The value of a++ is: " + (a++));
console.log("Now the value of a is: " + a + "\n");

console.log("The value of --a is: " + (--a));
console.log("Now the value of a is: " + a + "\n");

console.log("The value of a-- is: " + (a--));
console.log("Now the value of a is: " + a);


